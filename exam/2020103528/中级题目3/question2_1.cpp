// 什么是纯虚函数和抽象类？它们如何与虚函数表和虚指针相关联？
// 1. 纯虚函数：
// 纯虚函数是在基类中声明的虚函数，它没有具体的实现，并在派生类中被重写。语法上用 virtual 关键字声明，并在函数后面加上 = 0，表示纯虚函数。
class Base {
public:
    virtual void pureVirtualFunction() = 0;  
};
// 2. 抽象类：
// 抽象类是包含至少一个纯虚函数的类。抽象类不能被实例化，只能被用作基类，被继承并在派生类中实现纯虚函数。
class Derived : public Base {
public:
    void pureVirtualFunction() override {}
};
// 每个有虚函数的类都有一个与之相关联的虚函数表，它是一个包含虚函数地址的表。这个表在编译时生成，用于在运行时解析虚函数的调用。
// 每个对象都有一个虚指针，指向其类的虚函数表。虚指针通常在对象的内存布局的开头，被用于动态查找虚函数。